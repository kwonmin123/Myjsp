package sample03javabean;

public class Bean04 {
String nickName;
int age;
boolean vip;
public String getNickName() {
	return nickName;
}
public void setNickName(String nickName) {
	this.nickName = nickName;
}
public int getAge() {
	return age;
}
public void setAge(int age) {
	this.age = age;
}
public boolean isVip() {
	return vip;
}
public void setVip(boolean vip) {
	this.vip = vip;
}


}
